static void print_hex(const char* label, const uint8_t* data, size_t len) {
    printf("%s", label);
    for (size_t i = 0; i < len; i++) {
        printf("%02X", data[i]);
    }
    putchar('\n');
}

/* Test AES in ECB mode with known plaintext/ciphertext */
static void ecb_test(void) {
    /* Test vector: https://www.kavaliro.com/wp-content/uploads/2014/03/AES.pdf */
    AES_ctx ctx;
    uint8_t key[AES_KEY_SIZE] = {
        0x54, 0x68, 0x61, 0x74,   // "That"
        0x73, 0x20, 0x6D, 0x79,   // "s my"
        0x20, 0x4B, 0x75, 0x6E,   // " Kun"
        0x67, 0x20, 0x46, 0x75    // "g Fu"
    };

    uint8_t plaintext[AES_BLOCK_SIZE * 2] = {
        0x54, 0x77, 0x6F, 0x20, 0x4F, 0x6E, 0x65, 0x20,
        0x4E, 0x69, 0x6E, 0x65, 0x20, 0x54, 0x77, 0x6F,
        0x54, 0x77, 0x6F, 0x20, 0x4F, 0x6E, 0x65, 0x20,
        0x4E, 0x69, 0x6E, 0x65, 0x20, 0x54, 0x77, 0x6F
    };

    uint8_t ciphertext[sizeof(plaintext)];
    uint8_t decrypted[sizeof(plaintext)];

    AES_ctx_init(&ctx, AES_MODE_ECB, key);
    AES_encrypt(&ctx, plaintext, ciphertext, sizeof(plaintext));
    AES_decrypt(&ctx, ciphertext, decrypted, sizeof(ciphertext));

    print_hex("Key: ", key, AES_KEY_SIZE);
    print_hex("Input: ", plaintext, sizeof(plaintext));
    print_hex("Encrypted: ", ciphertext, sizeof(ciphertext));
    print_hex("Decrypted: ", decrypted, sizeof(decrypted));

    if (memcmp(plaintext, decrypted, sizeof(plaintext)) == 0)
        puts("[ECB] PASS");
    else
        puts("[ECB] FAIL");
}

/* Test AES in CBC mode using NIST SP 800-38A vectors */
static void cbc_test(void) {
    AES_ctx ctx;

    uint8_t key[AES_KEY_SIZE] = {
        0x2b, 0x7e, 0x15, 0x16, 0x28, 0xae, 0xd2, 0xa6,
        0xab, 0xf7, 0x15, 0x88, 0x09, 0xcf, 0x4f, 0x3c
    };

    uint8_t iv[AES_BLOCK_SIZE] = {
        0x00, 0x01, 0x02, 0x03, 0x04, 0x05, 0x06, 0x07,
        0x08, 0x09, 0x0a, 0x0b, 0x0c, 0x0d, 0x0e, 0x0f
    };

    uint8_t plaintext[64] = {
        0x6b, 0xc1, 0xbe, 0xe2, 0x2e, 0x40, 0x9f, 0x96,
        0xe9, 0x3d, 0x7e, 0x11, 0x73, 0x93, 0x17, 0x2a,
        0xae, 0x2d, 0x8a, 0x57, 0x1e, 0x03, 0xac, 0x9c,
        0x9e, 0xb7, 0x6f, 0xac, 0x45, 0xaf, 0x8e, 0x51,
        0x30, 0xc8, 0x1c, 0x46, 0xa3, 0x5c, 0xe4, 0x11,
        0xe5, 0xfb, 0xc1, 0x19, 0x1a, 0x0a, 0x52, 0xef,
        0xf6, 0x9f, 0x24, 0x45, 0xdf, 0x4f, 0x9b, 0x17,
        0xad, 0x2b, 0x41, 0x7b, 0xe6, 0x6c, 0x37, 0x10
    };

    uint8_t expected[64] = {
        0x76, 0x49, 0xab, 0xac, 0x81, 0x19, 0xb2, 0x46,
        0xce, 0xe9, 0x8e, 0x9b, 0x12, 0xe9, 0x19, 0x7d,
        0x50, 0x86, 0xcb, 0x9b, 0x50, 0x72, 0x19, 0xee,
        0x95, 0xdb, 0x11, 0x3a, 0x91, 0x76, 0x78, 0xb2,
        0x73, 0xbe, 0xd6, 0xb8, 0xe3, 0xc1, 0x74, 0x3b,
        0x71, 0x16, 0xe6, 0x9e, 0x22, 0x22, 0x95, 0x16,
        0x3f, 0xf1, 0xca, 0xa1, 0x68, 0x1f, 0xac, 0x09,
        0x12, 0x0e, 0xca, 0x30, 0x75, 0x86, 0xe1, 0xa7
    };

    uint8_t ciphertext[sizeof(plaintext)];
    uint8_t decrypted[sizeof(plaintext)];

    AES_ctx_init_iv(&ctx, AES_MODE_CBC, key, iv);
    AES_encrypt(&ctx, plaintext, ciphertext, sizeof(plaintext));

    print_hex("CBC Encrypted: ", ciphertext, sizeof(ciphertext));
    if (memcmp(ciphertext, expected, sizeof(expected)) == 0)
        puts("[CBC] Encryption PASS");
    else
        puts("[CBC] Encryption FAIL");

    AES_ctx_set_iv(&ctx, iv);
    AES_decrypt(&ctx, ciphertext, decrypted, sizeof(ciphertext));

    if (memcmp(decrypted, plaintext, sizeof(plaintext)) == 0)
        puts("[CBC] Decryption PASS");
    else
        puts("[CBC] Decryption FAIL");
}

int main(void) {
    ecb_test();
    cbc_test();
    return 0;
}

